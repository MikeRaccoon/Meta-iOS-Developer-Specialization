// Task 1
for (i = 1; i <= 6; i++) {
    if (i == 6) {
        console.log("Task 1: Counting completed!")
    } else {
        console.log(i)
    }
}

// Task 2
for (i = 5; i >= 0; i--) {
    if (i == 0) {
        console.log("Task 2: Countdown finised!")
    } else {
        console.log(i)
    }
}

// Task 3
var i = 0
while (i <= 5) {
    i += 1

    if (i == 6) {
        console.log("Task 3: Counting completed!")
    } else {
        console.log(i)
    }
}

// Task 4
while (i > 0) {
    i -= 1

    if (i == 0) {
        console.log("Task 4: Countdown finised!")
    } else {
        console.log(i)
    }
}

